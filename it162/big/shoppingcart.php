<h3>The Importance of a Shopping Cart</h3>
<p>The ability to make purchase decisions. The cart is a holding area to collect 
    items that a buyer is considering purchasing. From the cart, the buyer has an easier
    time making determinations and comparing items.</p>
<p>A cart allow merchants to offer discounts, whether they are first time buyers or 
    frequent shoppers on the site. This provides return visitors to the website.</p>
<p>Order lists help make final confirmations to the buyer just in case they clicked on
    the wrong size, color, quantity, or product in general to which they can easily
    delete and reselect the correct product.</p>
<p>The cart allows easy removal of items by having a remove button or checkbox option 
    to take the item out of the cart.</p>
