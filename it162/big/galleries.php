<h2>What JQuery Sliders Do</h2>
<p>The JSlider is a component for selecting a value in a numerical range from 0 to 100
    with 50 also being an initial value on the slider. You have the ability to change 
    any defaults, the direction of the scrollbar, and the tick marks that appear next to
    them to make the features appear anyway you like.</p>
