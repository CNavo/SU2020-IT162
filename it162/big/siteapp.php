<h2>Responsive vs Mobile</h2>
<p>To understand the comparisons between Reponsive Websites versus Mobile Apps 
let's first look at brief descriptions for each.
</p>
<br>
<h3>Responsive Websites</h3>
<p>Responsive Website Design or RWD is one designed to respond, based on a type
of technical device used, to display a visited site. The idea is for visitors to have
the same shared experience, regardless what size their device is, that will be equally
accessible to navigate, read, and view the content.
</p>
<br>
<h3>Mobile Apps</h3>
<p>Mobile Applications are downloadable software programs that run specifically on
mobile devices such as phones and tablets. These apps are often designed for specific
purposes such as a game, banking, calendar planning, etc.</p>
<br>
<h3>The Pros & Cons of Responsive Websites</h3>
<div class="pc_row">
  <div class="pc_column">
    <h4>Pros</h4>
    <ul>
    <li>Accessible on all browsers including mobile devices</li>
    <li>No downloads or required installations to access</li>
    <li>Easier to update with new information</li>
    <li>Cheaper in cost to maintain</li>
    <li>Great for building a new customer base</li>
    </ul>
  </div>
  <div class="pc_column">
    <h4>Cons</h4>
    <ul>
    <li>Internet access is required</li>
    <li>Lack of push notifications to get a user's attention</li>
    <li>Natural Navigation often gets lost on mobile and other smaller devices</li>
    <li>Some elements such as images take up a huge consumption of battery and data on mobile devices</li>
    </ul>
  </div>
</div>
<br>
<h3>The Pros & Cons of Mobile Apps</h3>
<div class="pc_row">
  <div class="pc_column">
    <h4>Pros</h4>
    <ul>
    <li>Accessible almost at anytime sometimes even without internet access.</li>
    <li>Great for an established customer base</li>
    <li>Apps are good for the needs of the individual</li>
    <li>Better security for users since connections do not require a third party</li>
    <li>Elements are designed for mobile performance, so loading times are quicker</li>
    </ul>
  </div>
  <div class="pc_column">
    <h4>Cons</h4>
    <ul>
    <li>Only accessible through phones or tablets</li>
    <li>Difficult to update the content or fix bugs and other issues</li>
    <li>Requires to download and install</li>
    <li>More expensive to build</li>
    <li>Approval processes tend to take more time depending on each OS requirement</li>
    </ul>
  </div>
</div>
<h3>Conclusion</h3>
<p> The comparison as to which is a better option depends on what one wants the site to
do. Ideally, responsive websites are great for new exposure for clients to find you, 
cheaper startup costs, and available to access on most browsers. The Mobile App option
helps with the individual mindset with a more engaging experience, specific operations
leading to a faster performance. Of course a combination of both, would answer all 
required needs but again it comes down to the end goal of the developer and client.
</div>
